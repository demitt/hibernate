package ua.skillsup.practice.hibernate.model.dto;

import java.util.List;
import java.util.Objects;

public class ItemDto {
	private Long id;
	private String title;
	private String description;
	private Double width;
	private Double height;
	private Double weight;
	private List<CategoryDto> categories;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Double getWidth() {
		return width;
	}

	public void setWidth(Double width) {
		this.width = width;
	}

	public Double getHeight() {
		return height;
	}

	public void setHeight(Double height) {
		this.height = height;
	}

	public Double getWeight() {
		return weight;
	}

	public void setWeight(Double weight) {
		this.weight = weight;
	}

	public List<CategoryDto> getCategories() {
		return categories;
	}

	public void setCategories(List<CategoryDto> categories) {
		this.categories = categories;
	}

	@Override
	public boolean equals(Object o) {
		if (this == o) return true;
		if (o == null || getClass() != o.getClass()) return false;
		ItemDto itemDto = (ItemDto) o;
		return Objects.equals(id, itemDto.id) &&
				Objects.equals(title, itemDto.title) &&
				Objects.equals(description, itemDto.description) &&
				Objects.equals(width, itemDto.width) &&
				Objects.equals(height, itemDto.height) &&
				Objects.equals(weight, itemDto.weight);
	}

	@Override
	public int hashCode() {
		return Objects.hash(id, title, description, width, height, weight);
	}

	@Override
	public String toString() {
		return "ItemDto{" +
			"id=" + id +
			", title='" + title + '\'' +
			", description='" + description + '\'' +
			", width=" + width +
			", height=" + height +
			", weight=" + weight +
			", categories=" + categories +
			'}';
	}
}